int	ft_atoi(char *str)
{
	int	i;
	int	a;
	int	z;

i = 0;
a = 1;
z = 0;
	while ((str[i] >= 7 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == 45 || str[i] == 43)
	{
		if (str[i] == 45)
			a *= -1;
	i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{	
		z = z * 10 + (str[i] - '0');
		i++;
	}
z *= a;
	return (z);
}
